export type Language = 'pt-BR' | 'en' | 'es';

export const useTranslation = (lang: string = 'pt-BR') => {
  const safeLang = (translations as any)[lang] ? lang : 'pt-BR';
  const t = (key: keyof typeof translations['pt-BR']) => {
    return translations[safeLang as Language][key] || translations['pt-BR'][key] || key;
  };
  return { t };
};
